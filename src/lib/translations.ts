export const getGreeting = (lang: Language) => {
    const hour = new Date().getHours();
    const t = translations[lang].greetings;
    const pick = (arr: string[]) => arr[Math.floor(Math.random() * arr.length)];

    if (hour === 3 || hour === 4) return pick(t.night);
    if (hour >= 23 || hour < 3) return pick(t.late);
    if (hour >= 5 && hour < 12) return pick(t.morning);
    if (hour >= 12 && hour < 18) return pick(t.afternoon);
    return pick(t.evening);
};
